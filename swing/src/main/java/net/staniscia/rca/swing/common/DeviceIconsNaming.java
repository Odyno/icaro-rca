package net.staniscia.rca.swing.common;

/**
 * This Icon Selector is complian to standards http://standards.freedesktop.org
 * 
 * @see http 
 *      ://standards.freedesktop.org/icon-naming-spec/icon-naming-spec-latest
 *      .html for more information
 * 
 * @version Version 0.8.90
 */
public class DeviceIconsNaming{

	public String CONTEXT_ACTIONS = "devices";

	/** The icon used for the audio rendering device. */
	public static final String audio_card = "audio-card";
	/** The icon used for the microphone audio input device. */
	public static final String audio_input_microphone = "audio-input-microphone";
	/** The icon used for the system battery device. */
	public static final String battery = "battery";
	/** The icon used for a digital still camera devices. */
	public static final String camera_photo = "camera-photo";
	/** The fallback icon for video cameras. */
	public static final String camera_video = "camera-video";
	/** The fallback icon for web cameras. */
	public static final String camera_web = "camera-web";
	/** The icon used for the computing device as a whole. */
	public static final String computer = "computer";
	/** The icon used for hard disk drives. */
	public static final String drive_harddisk = "drive-harddisk";
	/** The icon used for optical media drives such as CD and DVD. */
	public static final String drive_optical = "drive-optical";
	/** The icon used for removable media drives. */
	public static final String drive_removable_media = "drive-removable-media";
	/** The icon used for the gaming input device. */
	public static final String input_gaming = "input-gaming";
	/** The icon used for the keyboard input device. */
	public static final String input_keyboard = "input-keyboard";
	/** The icon used for the mousing input device. */
	public static final String input_mouse = "input-mouse";
	/** The icon used for graphics tablet input devices. */
	public static final String input_tablet = "input-tablet";
	/** The fallback icon used for flash media, such as memory stick and SD. */
	public static final String media_flash = "media-flash";
	/** The icon used for physical floppy disk media. */
	public static final String media_floppy = "media-floppy";
	/** The icon used for physical optical media such as CD and DVD. */
	public static final String media_optical = "media-optical";
	/** The icon used for generic physical tape media. */
	public static final String media_tape = "media-tape";
	/** The icon used for modem devices. */
	public static final String modem = "modem";
	/** The icon used for generic multimedia playing devices. */
	public static final String multimedia_player = "multimedia-player";
	/** The icon used for wired network connections. */
	public static final String network_wired = "network-wired";
	/** The icon used for wireless network connections. */
	public static final String network_wireless = "network-wireless";
	/**
	 * This is the fallback icon for Personal Digial Assistant devices. Primary
	 * use of this icon is for PDA devices connected to the PC. Connection
	 * medium is not an important aspect of the icon. The metaphor for this
	 * fallback icon should be a generic PDA device icon.
	 */
	public static final String pda = "pda";
	/**
	 * This is the default fallback for phone devices. Primary use of this icon
	 * group is for phone devices which support connectivity to the PC. These
	 * may be VoIP, cellular, or possibly landline phones. The metaphor for this
	 * fallback should be a generic mobile phone device.
	 */
	public static final String phone = "phone";
	/** The icon used for a printer device. */
	public static final String printer = "printer";
	/** The icon used for a scanner device. */
	public static final String scanner = "scanner";
	/** The icon used for the monitor that video gets displayed to. */
	public static final String video_display = "video-display";

}
